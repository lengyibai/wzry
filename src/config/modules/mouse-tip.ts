/** @description 模板Tip
 * @param key Tip的键名
 * @param variables 替换Tip中的key值
 */
const $mouseTipText = (key: keyof typeof MOUSE_TIP, variables: Record<string, string | number>) => {
  let template = MOUSE_TIP[key];
  Object.entries(variables).forEach(([key, value]) => {
    template = template.replaceAll(
      `{${key}}`,
      value.toString(),
    ) as (typeof MOUSE_TIP)[keyof typeof MOUSE_TIP];
  });
  return template;
};

export { MOUSE_TIP, $mouseTipText };
